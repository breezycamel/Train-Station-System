package trainstation.model;

public class Reservation {
	private static final long serialVersionUID = 1L;
	private String userName;
    private String trainId;
    private String origin;
    private String destination;
    private String tripType;
    private String travelDate;
    private String originTime;
    private String destinationTime;
    private int fare;
    private String ticketType;
    
    public Reservation(String userName, String trainId, String origin, String destination, String tripType, String travelDate, String originTime, String destinationTime, int fare, String ticketType) {
    	this.userName = userName;
    	this.trainId = trainId;
    	this.origin = origin;
    	this.destination = destination;
    	this.tripType = tripType;
    	this.travelDate = travelDate;
    	this.originTime = originTime;
    	this.destinationTime = destinationTime;
    	this.fare = fare;
    	this.ticketType = ticketType;
    }
    
    public String getUsername() {
        return userName;
    }
    public void setUsername(String userName) {
        this.userName = userName;
    }
    
    public String gettrainId() {
        return trainId;
    }
    public void settrainId(String trainId) {
        this.trainId = trainId;
    }
    
    public String getOrigin() {
        return origin;
    }
    public void setOrigin(String origin) {
        this.origin = origin;
    }
    
    public String getDestination() {
        return destination;
    }
    public void setDestination(String destination) {
        this.destination = destination;
    }
    
    public int getFare() {
    	return fare;
    }
    public void setFare(int fare) {
    	this.fare = fare;
    }
    
    public String getTripType() {
        return tripType;
    }
    public void setTripType(String tripType) {
        this.tripType = tripType;
    }
    
    public String getTicketType() {
        return ticketType;
    }
    public void setTicketType(String ticketType) {
        this.ticketType = ticketType;
    }
    
    public String getTravelDate() {
    	return travelDate;
    }
    
    public void setTravelDate(String travelDate) {
    	this.travelDate = travelDate;
    }
    
    public String getOriginTime() {
    	return originTime;
    }
    public void setOriginTime(String originTime) {
    	this.originTime = originTime;
    }
    
    public String getDestinationTime() {
    	return destinationTime;
    }
    public void setDestinationTime(String destinationTime) {
    	this.destinationTime = destinationTime;
    }
}
